import { promises as fs } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

import { fitMonotonePricingModel } from "../../lib/monotone-pricing-model.js";

const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = process.env.TRACO_BASE_REPO
  ? path.resolve(process.env.TRACO_BASE_REPO)
  : path.resolve(scriptDir, "../..");

const datasetPath = path.resolve(
  process.env.ORCA_DATASET_PATH ||
    path.join(repoRoot, "Produtos", "datasets", "slicer_pricing_dataset.csv")
);
const outputPath = path.join(repoRoot, "lib", "slicer-pricing-data.js");

const parameterKeys = [
  "diametro",
  "diametroBase",
  "tamanhoBaseX",
  "tamanhoBaseY",
  "alturaBase",
  "alturaPescoco",
  "diametroPescoco",
  "paredeTubo",
  "pescoco"
];

async function main() {
  const { headers, rows } = await readDataset(datasetPath);
  const required = [
    "sample_id",
    "category_slug",
    "format_slug",
    "variant_slug",
    "has_neck",
    "material_grams",
    "print_minutes",
    "slice_status"
  ];

  for (const header of required) {
    if (!headers.includes(header)) {
      throw new Error(`Coluna obrigatoria ausente no dataset: ${header}`);
    }
  }

  const samples = rows
    .filter((row) => {
      return (
        row.slice_status === "ok" &&
        Number(row.material_grams || 0) > 0 &&
        Number(row.print_minutes || 0) > 0
      );
    })
    .map(toSiteSample);

  if (samples.length === 0) {
    throw new Error("Nenhuma amostra Orca valida encontrada para o site.");
  }

  const surfaceCounts = samples.reduce((counts, sample) => {
    const key = `${sample.categorySlug}:${sample.formatSlug}:${sample.variantSlug}`;
    counts[key] = (counts[key] || 0) + 1;
    return counts;
  }, {});
  const pricingModels = await buildPricingModels(samples);

  const source = [
    "// Generated by Produtos/scripts/build-site-pricing-data.mjs.",
    "// Do not edit manually; update Produtos/datasets/slicer_pricing_dataset.csv and rerun npm run pricing:build-data.",
    `export const slicerPricingDataVersion = ${JSON.stringify(new Date().toISOString())};`,
    `export const slicerPricingSource = ${JSON.stringify("Produtos/datasets/slicer_pricing_dataset.csv")};`,
    `export const slicerPricingSurfaceCounts = ${JSON.stringify(surfaceCounts)};`,
    `export const slicerPricingModels = ${JSON.stringify(pricingModels)};`,
    `export const slicerPricingSamples = ${JSON.stringify(samples)};`,
    ""
  ].join("\n");

  await fs.writeFile(outputPath, source);
  console.log(`Dados de slice para o site: ${outputPath}`);
  console.log(`Amostras: ${samples.length}`);
  console.log(`Modelos monotonos: ${Object.keys(pricingModels).length}`);
}

async function buildPricingModels(samples) {
  const catalogDirectory = path.join(repoRoot, "catalog", "products");
  const manifestNames = (await fs.readdir(catalogDirectory)).filter((name) => name.endsWith(".json"));
  const models = {};

  for (const manifestName of manifestNames) {
    const manifest = JSON.parse(await fs.readFile(path.join(catalogDirectory, manifestName), "utf8"));
    if (manifest.status !== "active") {
      continue;
    }

    for (const variant of manifest.variants || []) {
      if (!variant.public || variant.pricing?.strategy !== "sliced_dataset") {
        continue;
      }

      const surfaceId = variant.pricing.surfaceId;
      const surfaceSamples = samples.filter((sample) => {
        return `${sample.categorySlug}:${sample.formatSlug}:${sample.variantSlug}` === surfaceId;
      });
      const wallKey = variant.cad?.sliderOrder?.includes("paredeTubo") ? "paredeTubo" : "";
      const progressiveKeys = (variant.cad?.sliderOrder || []).filter((key) => key !== wallKey);

      models[surfaceId] = fitMonotonePricingModel(surfaceSamples, {
        surfaceId,
        parameterKeys: progressiveKeys,
        wallKey,
        degree: 3
      });
    }
  }

  return models;
}

function toSiteSample(row) {
  const params = {};

  for (const key of parameterKeys) {
    const rawValue = row[key];

    if (rawValue === undefined || rawValue === "") {
      continue;
    }

    params[key] = key === "pescoco" ? rawValue === "true" : roundMetric(Number(rawValue));
  }

  return {
    sampleId: row.sample_id,
    categorySlug: row.category_slug,
    formatSlug: row.format_slug,
    variantSlug: row.variant_slug || (row.has_neck === "true" ? "haste" : "sem-haste"),
    hasNeck: row.has_neck === "true",
    params,
    materialGrams: roundMetric(Number(row.material_grams || 0), 2),
    printMinutes: roundMetric(Number(row.print_minutes || 0), 2),
    productionCostBrl: roundMetric(Number(row.production_cost_brl || 0), 2),
    profileId: row.profile_id || "",
    orcaVersion: row.orca_version || ""
  };
}

async function readDataset(filePath) {
  const text = await fs.readFile(filePath, "utf8");
  const records = parseCsv(text);
  const headers = records[0] || [];
  const rows = records.slice(1).map((record) => {
    return Object.fromEntries(headers.map((header, index) => [header, record[index] ?? ""]));
  });

  return { headers, rows };
}

function parseCsv(text) {
  const records = [];
  let record = [];
  let field = "";
  let quoted = false;

  for (let index = 0; index < text.length; index += 1) {
    const char = text[index];
    const next = text[index + 1];

    if (quoted) {
      if (char === '"' && next === '"') {
        field += '"';
        index += 1;
      } else if (char === '"') {
        quoted = false;
      } else {
        field += char;
      }
      continue;
    }

    if (char === '"') {
      quoted = true;
    } else if (char === ",") {
      record.push(field);
      field = "";
    } else if (char === "\n") {
      record.push(field);
      records.push(record);
      record = [];
      field = "";
    } else if (char !== "\r") {
      field += char;
    }
  }

  if (field || record.length > 0) {
    record.push(field);
    records.push(record);
  }

  return records;
}

function roundMetric(value, decimals = 2) {
  const factor = Math.pow(10, decimals);
  return Math.round(Number(value || 0) * factor) / factor;
}

main().catch((error) => {
  console.error(error.message);
  process.exit(1);
});
