import fs from "node:fs/promises";
import path from "node:path";

const root = path.resolve(process.cwd(), "pricing-lab");
const outputPath = path.resolve(process.cwd(), "lib", "sliced-pricing-data.js");

const samples = await readCsv(path.join(root, "inputs", "sample-grid.csv"));
const results = await readCsv(path.join(root, "results", "orca-results.csv"));

const samplesByKey = new Map(samples.map((row) => [key(row), row]));
const rows = results
  .map((result) => {
    const sample = samplesByKey.get(key(result));
    if (!sample) return null;

    return {
      familySlug: sample.family_slug,
      variantSlug: sample.variant_slug,
      sampleId: sample.sample_id,
      siteCategorySlug: mapSiteCategory(sample),
      siteFormatSlug: mapSiteFormat(sample),
      dimensions: {
        diametro: number(sample.diameter_mm),
        altura: number(sample.height_mm),
        diametroFuro: number(sample.hole_diameter_mm)
      },
      quantity: number(sample.quantity) || 1,
      modelFile: sample.model_file,
      gcodeFile: result.gcode_file,
      materialGrams: number(result.material_grams),
      printMinutes: number(result.print_minutes),
      orcaVersion: result.orca_version,
      profileId: result.profile_id,
      slicedAt: result.sliced_at
    };
  })
  .filter((row) => row && row.materialGrams > 0 && row.printMinutes > 0);

if (rows.length === 0) {
  throw new Error(
    "Nenhuma linha com material_grams e print_minutes foi encontrada; lib/sliced-pricing-data.js nao foi alterado."
  );
}

const contents = `// Generated by pricing-lab/scripts/build-sliced-pricing-data.mjs.
// Source: pricing-lab/results/orca-results.csv.

export const slicedPricingReferences = ${JSON.stringify(rows, null, 2)};
`;

await fs.writeFile(outputPath, contents);
console.log(`Atualizado: ${outputPath}`);

function mapSiteCategory(sample) {
  if (sample.family_slug === "sapata-lisa-redonda") return "sapata-base-lisa";
  return "";
}

function mapSiteFormat(sample) {
  if (sample.family_slug === "sapata-lisa-redonda") return "redonda";
  return "";
}

function key(row) {
  return `${row.family_slug}::${row.sample_id}`;
}

function number(value) {
  const parsed = Number(String(value || "").replace(",", "."));
  return Number.isFinite(parsed) ? parsed : 0;
}

async function readCsv(filePath) {
  const text = await fs.readFile(filePath, "utf8");
  const rows = parseCsv(text);
  const headers = rows.shift() || [];
  return rows
    .filter((row) => row.some((value) => value !== ""))
    .map((row) => Object.fromEntries(headers.map((header, index) => [header, row[index] || ""])));
}

function parseCsv(text) {
  const rows = [];
  let row = [];
  let cell = "";
  let quoted = false;

  for (let index = 0; index < text.length; index += 1) {
    const char = text[index];
    const next = text[index + 1];

    if (char === "\"" && quoted && next === "\"") {
      cell += "\"";
      index += 1;
    } else if (char === "\"") {
      quoted = !quoted;
    } else if (char === "," && !quoted) {
      row.push(cell);
      cell = "";
    } else if ((char === "\n" || char === "\r") && !quoted) {
      if (char === "\r" && next === "\n") index += 1;
      row.push(cell);
      rows.push(row);
      row = [];
      cell = "";
    } else {
      cell += char;
    }
  }

  if (cell || row.length) {
    row.push(cell);
    rows.push(row);
  }

  return rows;
}
